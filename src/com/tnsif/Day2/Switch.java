package com.tnsif.Day2;

public class Switch {

	public static void main(String[] args) {
		char a='s';
		switch(a) {
		case 'k':
		case 'K':
			System.out.println("KIND");
			break;
		case 'd':
		case 'D':
			System.out.println("DIVINE");
			break;
		default:
			System.out.println("NONE");
					
		}
		

	}

}
